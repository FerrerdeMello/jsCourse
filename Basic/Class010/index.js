/**
 * ARITMÉTICOS
 * + adição / concatenação
 * -  /   *
 * potenciação **
 * resto da divisão %
 * Incremento = ++
 * Decremento = --
 * Oepradores de atribuição
 * +=   -=   *=   /=   **=  
 */
const num1 = 5;
const num2 = 10;
let num3 = 4;

console.log(num1 - num2);
console.log(num2 ** num1);
console.log(num1 % num3);
++num3;
console.log(num3);
num3 *= 3;
console.log(num3);
